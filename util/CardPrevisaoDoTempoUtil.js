export const nomeDodiaDaSemana = (numeroDoDia) => {
    if (numeroDoDia === 1) return "Segunda-feira";
    else if (numeroDoDia === 2) return "Terça-feira";
    else if (numeroDoDia === 3) return "Quarta-feira";
    else if (numeroDoDia === 4) return "Quinta-feira";
    else if (numeroDoDia === 5) return "Sexta-feira";
    else if (numeroDoDia === 6) return "Sábado";
    else return "Domingo";
  };
  